/*
C++ program to add two numbers: This c++ language program perform the basic arithmetic operation
of addition on two numbers and then prints the sum on the screen. For example if the user entered
two numbers as 5, 6 then 11 ( 5 + 6 ) will be printed on the screen. */
#include<iostream.h>
 
int main()
{
   int a, b, c;
 
   cout<<"Enter two numbers to add\n";
   cin>>a>>b;
 
   c = a + b;
 
   cout<<"Sum of entered numbers = "<<c;
 
   return 0;
}
